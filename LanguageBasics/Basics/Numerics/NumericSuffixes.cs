namespace Basics.Numerics;

public class NumericSuffixes {
    public static void Suffixes() {
        Console.WriteLine("float => Suffix: F. Example: float f = 1.0F");
        Console.WriteLine("double => Suffix: D. Example: double d = 1D");
        Console.WriteLine("decimal => Suffix: M. Example: decimal d = 1.0M");
        Console.WriteLine("uint => Suffix: U. Example: uint u = 1U");
        Console.WriteLine("long => Suffix: L. Example: long l = 1L");
        Console.WriteLine("ulong => Suffix: UL. Example: ulong u = 1UL");
    }
}